namespace Ess\M2ePro\Model\ActiveRecord;

/**
 * Class Cache
 * @package Ess\M2ePro\Model\ActiveRecord
 */
class Cache
{
    protected $data = [];

    //########################################

    public function __construct($data)
    {
        $this->data = $data;
    }

    //########################################

    /**
     * @return array
     */
    public function getData()
    {
        return $this->data;
    }

    /**
     * @param array $data
     */
    public function setData($data)
    {
        $this->data = $data;
    }

    //########################################
}
